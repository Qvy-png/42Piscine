char	*ft_strcapitalize(char *str)
{
	unsigned int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{	
			str[i] += 32;
		}
		if (i == 0 || (str[i - 1] >= ' ' && str[i - 1] < '0')
			|| (str[i - 1] >= 9 && str[i - 1] <= 13)
			|| (str[i - 1] >= ':' && str[i - 1] < 'A')
			|| (str[i - 1] >= '[' && str[i - 1] < 'a')
			|| (str[i - 1] >= '{' && str[i - 1] < 127))
		{
			if (str[i] >= 'a' && str[i] <= 'z')
				str[i] -= 32;
		}
		i++;
	}
	return (str);
}
